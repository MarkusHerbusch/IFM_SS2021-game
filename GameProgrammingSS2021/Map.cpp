Map::Map()
{
	limitation = TextureManager::LoadTexture("images/limitation.png");
	background = TextureManager::LoadTexture("images/background.png");
	backgroundLimitRight = TextureManager::LoadTexture("images/backgroundLimitRight.png");
	backgroundLimitBelow = TextureManager::LoadTexture("images/backgroundLimitBelow.png");
	
	LoadMap(level1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[25][50])
{
	for (int row = 0; row < 25; row++)
	{
		for (int column = 0; column < 50; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 25; row++)
	{
		for (int column = 0; column < 50; column++)
		{
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 1:
				TextureManager::Draw(limitation, src, dest);
				break;
			case 2:
				TextureManager::Draw(background, src, dest);
				break;
			case 3:
				TextureManager::Draw(backgroundLimitRight, src, dest);
				break;
			case 4:
				TextureManager::Draw(backgroundLimitBelow, src, dest);
				break;
			default:
				break;
			}
		}
	}
}

